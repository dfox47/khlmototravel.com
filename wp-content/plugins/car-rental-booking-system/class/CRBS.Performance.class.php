class CRBSPerformance
{
	/**************************************************************************/
	
	static function start()
	{
		global $crbs_performance_timer;
		$crbs_performance_timer=microtime(true); 
	}
	
	/**************************************************************************/
	
	static function stop()
	{
		global $crbs_performance_timer;
		
		$time=microtime(true)-$crbs_performance_timer;
		
		echo sprintf(esc_html__('Total time: %s','car-rental-booking-system'),$time).'<br>';
	}
	
	/**************************************************************************/
}

