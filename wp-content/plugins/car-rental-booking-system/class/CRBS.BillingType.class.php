/******************************************************************************/

class CRBSBillingType
{
    /**************************************************************************/
    
    function __construct()
    {
        $this->billingType=array
        (
            1                                                                   =>  array(__('Hourly','car-rental-booking-system')),
            2                                                                   =>  array(__('Daily','car-rental-booking-system')),
            3                                                                   =>  array(__('Daily + hourly','car-rental-booking-system'))
        );
    }
    
    /**************************************************************************/
    
    function getDictionary()
    {
        return($this->billingType);
    }
    
    /**************************************************************************/
    
    function isBillingType($billingType)
    {
        return(array_key_exists($billingType,$this->billingType));
    }
    
    /**************************************************************************/
}

